// Helper function to get translation
export const t = (key: string, fallback?: string): string => {
  const keys = key.split('.')
  let value: unknown = translations
  
  for (const k of keys) {
    value = (value as Record<string, unknown>)?.[k]
    if (value === undefined) {
      return fallback || key
    }
  }
  
  return typeof value === 'string' ? value : fallback || key
}

// Helper function for pluralization
export const tp = (key: string, count: number, fallback?: string): string => {
  const translation = t(key, fallback)
  
  // Simple pluralization for Lithuanian
  if (count === 1) {
    return translation
  } else if (count > 1 && count < 10) {
    // For numbers 2-9, use plural form
    return translation.replace(/as$/, 'ai').replace(/ė$/, 'ės').replace(/is$/, 'iai')
  } else {
    // For numbers 10+, use plural form
    return translation.replace(/as$/, 'ų').replace(/ė$/, 'ių').replace(/is$/, 'ių')
  }
}
